    KEY idx_operation_log_workspace_id (workspace_id),
    KEY idx_operation_log_graph_id (graph_id),
    KEY idx_operation_log_task_id (task_id),
    KEY idx_operation_log_created_at (created_at),
    KEY idx_operation_log_operation_type (operation_type)
