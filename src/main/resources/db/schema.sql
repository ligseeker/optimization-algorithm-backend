CREATE TABLE IF NOT EXISTS constraint_condition (
    id BIGINT PRIMARY KEY AUTO_INCREMENT,
    graph_id BIGINT NOT NULL,
    condition_code VARCHAR(64) NOT NULL,
    condition_type VARCHAR(32) NOT NULL,
    condition_description VARCHAR(255) NULL,
    node_id_1 BIGINT NOT NULL,
    node_id_2 BIGINT NOT NULL,
    enabled TINYINT(1) NOT NULL DEFAULT 1,
    created_by BIGINT NOT NULL DEFAULT 0,
    updated_by BIGINT NOT NULL DEFAULT 0,
    created_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    deleted TINYINT(1) NOT NULL DEFAULT 0,
    UNIQUE KEY uk_constraint_condition_graph_code (graph_id, condition_code, deleted),
    UNIQUE KEY uk_constraint_condition_graph_pair_type (graph_id, node_id_1, node_id_2, condition_type, deleted),
    KEY idx_constraint_condition_graph_id (graph_id),
    KEY idx_constraint_condition_node1 (node_id_1),
    KEY idx_constraint_condition_node2 (node_id_2)
);

CREATE TABLE IF NOT EXISTS optimize_task (
    id BIGINT PRIMARY KEY AUTO_INCREMENT,
    task_no VARCHAR(64) NOT NULL,
    workspace_id BIGINT NOT NULL,
    graph_id BIGINT NOT NULL,
    user_id BIGINT NOT NULL,
    algorithm_type TINYINT NOT NULL,
    algorithm_mode TINYINT NOT NULL,
    time_weight INT NOT NULL DEFAULT 1,
    precision_weight INT NOT NULL DEFAULT 1,
    cost_weight INT NOT NULL DEFAULT 1,
    task_status VARCHAR(32) NOT NULL DEFAULT 'PENDING',
    retry_count INT NOT NULL DEFAULT 0,
    max_retry_count INT NOT NULL DEFAULT 3,
    queue_time DATETIME NOT NULL,
    started_at DATETIME NULL,
    finished_at DATETIME NULL,
    error_code VARCHAR(64) NULL,
    error_message VARCHAR(1000) NULL,
    result_id BIGINT NULL,
    created_by BIGINT NOT NULL DEFAULT 0,
    updated_by BIGINT NOT NULL DEFAULT 0,
    created_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    deleted TINYINT(1) NOT NULL DEFAULT 0,
    UNIQUE KEY uk_optimize_task_task_no (task_no),
    KEY idx_optimize_task_workspace_id (workspace_id),
    KEY idx_optimize_task_graph_id (graph_id),
    KEY idx_optimize_task_user_id (user_id),
    KEY idx_optimize_task_status_queue_time (task_status, queue_time)
);

